public class Main {
    public static void main(String[] args) {
        int a = 1;
        int b = 10;
        while (a <= 10) {
            System.out.print(a + " ");
            a = a + 1;
        }
        while (b >= 1) {
            System.out.print(b + " ");
            b = b - 1;
        }
    }
}